    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int i = communityTbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) communityTbl.getModel();
        if (i >= 0) {
            model.setValueAt(adminID.getText(), i, 0);
            model.setValueAt(commName.getText(), i, 1);
            model.setValueAt(selectRegion.getSelectedItem().toString(), i, 2);
            JOptionPane.showMessageDialog(null, "Updated Successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = communityTbl.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please Selected a Row to Delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) communityTbl.getModel();
        Hospital selectedPatient = (Hospital) model.getValueAt(selectedRowIndex, 0);
        hospitalhistory.deleteHospital(selectedPatient);
        JOptionPane.showMessageDialog(this, "Community Deleted");
        populateTable();
    }//GEN-LAST:event_deleteBtnActionPerformed


