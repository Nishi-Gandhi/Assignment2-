 private void populateTable() {
                DefaultTableModel model = (DefaultTableModel) communityTbl.getModel();
        model.setRowCount(0);
        for (Patient ps : patienthistory.getPatienthistory()) {
 
            Object[] row = new Object[2];
            row[0] = ps;
            row[1] = ps.getName();
            model.addRow(row);
        }
    }
