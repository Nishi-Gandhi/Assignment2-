 private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) patientTbl.getModel();
        model.setRowCount(0);
        for (Patient ps : patienthistory.getPatienthistory()) {
            Object[] row = new Object[8];
            row[0] = ps;
            row[1] = ps.getName();
            row[2] = ps.getAge();
            row[3]=ps.getGender();
            row[4] = ps.getEmailid();
            row[5] = ps.getAddress();
            row[6] = ps.getCity();
            row[7] = ps.getCellphoneno();
            model.addRow(row);
        }

    }
