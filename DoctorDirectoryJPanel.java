    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        int i = doctorTbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) doctorTbl.getModel();
        if (i >= 0) {
            model.setValueAt(doctorIDUpdate.getText(), i, 0);
            model.setValueAt(nameInputUpdate.getText(), i, 1);
            model.setValueAt(ageInputUpdate.getText(), i, 2);
            model.setValueAt(genderUpdate.getSelectedItem().toString(), i, 3);
            model.setValueAt(addressUpdate.getText(), i, 4);
            model.setValueAt(communityUpdate.getText(), i, 5);
            model.setValueAt(locationUpdate.getSelectedItem().toString(), i, 6);
            model.setValueAt(cellphoneNoUpdate.getText(), i, 7);
            model.setValueAt(emailIdUpdate.getText(), i, 8);
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

