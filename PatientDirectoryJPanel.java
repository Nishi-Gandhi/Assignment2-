    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = patientTbl.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please Selected a Row to Delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) patientTbl.getModel();
        Patient selectedPatient = (Patient) model.getValueAt(selectedRowIndex, 0);
        patienthistory.deletePatient(selectedPatient);
        JOptionPane.showMessageDialog(this, "Person Deleted");
        populateTable();
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        int i = patientTbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) patientTbl.getModel();
        if (i >= 0) {
            model.setValueAt(patientIDUpdate.getText(), i, 0);
            model.setValueAt(nameInputUpdate.getText(), i, 1);
            model.setValueAt(ageInputUpdate.getText(), i, 2);
            model.setValueAt(genderUpdate.getSelectedItem().toString(), i, 3);
            model.setValueAt(addressUpdate.getText(), i, 4);
            model.setValueAt(locationUpdate.getSelectedItem().toString(), i, 5);
            model.setValueAt(cellphoneNoUpdate.getText(), i, 6);
            model.setValueAt(emailIdUpdate.getText(), i, 7);
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel Model = (DefaultTableModel) patientTbl.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(Model);
        patientTbl.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(searchInput.getText().trim()));
    }//GEN-LAST:event_searchBtnActionPerformed

