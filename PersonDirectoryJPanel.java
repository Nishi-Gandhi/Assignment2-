    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int i = personTbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) personTbl.getModel();
        if (i >= 0) {
            model.setValueAt(personIDUpdate.getText(), i, 0);
            model.setValueAt(nameInputUpdate.getText(), i, 1);
            model.setValueAt(ageInputUpdate.getText(), i, 2);
            model.setValueAt(genderUpdate.getSelectedItem().toString(), i, 3);
            model.setValueAt(addressUpdate.getText(), i, 4);
            model.setValueAt(locationUpdate.getSelectedItem().toString(), i, 5);
            model.setValueAt(cellphoneNoUpdate.getText(), i, 6);
            model.setValueAt(emailIdUpdate.getText(), i, 7);
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

