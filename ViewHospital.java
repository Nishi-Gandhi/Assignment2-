private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) communityTbl.getModel();
        model.setRowCount(0);
        for (Hospital ps : hospitalhistory.getHospitalhistory()) {
            System.out.println(ps.getHospitalID());
            System.out.println(ps.getName());
            System.out.println(ps.getRegion());

            Object[] row = new Object[3];
            row[0] = ps;
            row[1] = ps.getName();
            row[2] = ps.getRegion();
            model.addRow(row);
        }
    }
