    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = personTbl.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please Selected a Row to Delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) personTbl.getModel();
        Person selectedPerson = (Person) model.getValueAt(selectedRowIndex, 0);
        personhistory.deletePerson(selectedPerson);
        JOptionPane.showMessageDialog(this, "Person Deleted");
        populateTable();
    }//GEN-LAST:event_DeleteButtonActionPerformed

